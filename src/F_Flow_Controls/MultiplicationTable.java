package F_Flow_Controls;

public class MultiplicationTable {
    public static void main(String[] args) {
        for (int i = 2; i < 10; i++) {
            for (int j = 2; j < 10; j++) {
                System.out.println(i + " x " + j + " = " + i * j);
            }
        }
    }
}
/*
i = 2, j = 2
i = 2, j = 3
 */